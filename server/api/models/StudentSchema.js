import mongoose from "mongoose";

const { Schema, model } = mongoose;

const StudentSchema = new Schema(
  {
    firstName: {
      type: String,
      required: true,
    },

    lastName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default model("Student", StudentSchema);
